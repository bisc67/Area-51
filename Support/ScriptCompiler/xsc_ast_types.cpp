//  ResolveTypes
//==============================================================================

static const xsc_symbol* ResolveType( const xsc_symbol* pType1, const xsc_symbol* pType2 )
{
    const xsc_symbol* pType = pType1;
    if( pType == g_pTs32 )
    {
        if( pType2 == g_pTf32 )
            pType = pType2;
    }

    return pType;
}

