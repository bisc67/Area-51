  // This routine will return a prime number slightly greater than
// the closest multiple of 1000.
s32 GeneratePrimeNear1000( s32 Value )
{
    s32 nPrimes = sizeof(s_Prime1000) / sizeof(byte);
    s32 I = Value/1000;
    if( I*1000 < Value )
        I++;

    //x_printf("MAX PRIME: %d\n",nPrimes*1000);
    //x_printf("PrimeTableByteSize: %d\n",sizeof(s_Prime1000));

    if( I < nPrimes )
    {
        return (I*1000 + s_Prime1000[I]);
    }
    else
    {
        ASSERT( I < nPrimes );
        return (I*1000);
    }
}

